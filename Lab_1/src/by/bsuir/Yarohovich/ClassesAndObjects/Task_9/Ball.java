package by.bsuir.Deschenya.ClassesAndObjects.Task_9;

public class Ball {

    private float weight;
    private String color;

    public Ball(float weight, String color) {
        this.weight = weight;
        this.color = color;
    }

    public float getWeight() {
        return weight;
    }

    public String getColor() {
        return color;
    }    
}
