package by.bsuir.Deschenya.fundamentals.Task_3;

final public class Section {
    private float a;
    private float b;
    private float h;

    public Section(float a, float b, float h) {
        this.a = a;
        this.b = b;
        this.h = h;
    }

    public float getA() {
        return a;
    }

    public float getB() {
        return b;
    }

    public float getH() {
        return h;
    }
}
