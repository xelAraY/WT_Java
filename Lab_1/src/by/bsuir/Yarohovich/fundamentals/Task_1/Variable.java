package by.bsuir.Deschenya.fundamentals.Task_1;

final public class Variable {
    private float x;
    private float y;

    public Variable(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }
}